package com.bairro.aula2;

public class TestaDeclaracoes {
	public static void main(String[] args) {
		
		byte variavelByte;
		variavelByte = 11;
		System.out.println("VariavelByte: " + variavelByte);
		
		short variavelShort = 1111;
		System.out.println("VariavelShort: " + variavelShort);
		
		int variavelInt = 1111111111;
		System.out.println("VariavelInt: " + variavelInt);
		
		long variavelLong = 111111111111111111L;
		System.out.println("VariavelLong: " + variavelLong);
		
		char variavelChar = 'a';
		System.out.println("VariavelChar: " + variavelChar);
	}
}
